const btnGuardar = document.getElementById('btnGuardar');
const btnModificar = document.getElementById('btnModificar');
const btnBuscar = document.getElementById('btnBuscar');
const btnCancelar = document.getElementById('btnCancelar');
const btnLimpiar = document.getElementById('btnLimpiar');
const tablaPros = document.getElementById('tablaPros');
const formulario = document.getElementById('formulario');

btnModificar.parentElement.style.display = 'none';
btnCancelar.parentElement.style.display = 'none';



const getPros = async (alerta ='si') => {
    const grado = formulario.pro_grado.value.trim();
    const arma = formulario.pro_arma.value.trim();
    const nombre = formulario.pro_nombre.value.trim();
    const apellido = formulario.pro_apellido.value.trim();
    const dpi = formulario.pro_dpi.value.trim();
    const correo = formulario.pro_correo.value.trim();

  
    const url = `/formaD_conJS/controllers/programador/index.php?pro_grado=${grado}&pro_arma=${arma}&pro_nombre=${nombre}&pro_apellido=${apellido}&pro_dpi=${dpi}&pro_correo${correo}`;
  
    const config = {
        method: 'GET'
    };

    try {
        const respuesta = await fetch(url, config);
        const data = await respuesta.json();
        console.log(data);

        tablaPros.tBodies[0].innerHTML = '';
        const fragment = document.createDocumentFragment();
        let contador = 1;

        if (respuesta.status === 200) {
            if(alerta =='si'){
                Swal.mixin({
                    toast: true,
                    position: "top-end",
                    showConfirmButton: false,
                    timer: 3000,
                    timerProgressBar: true,
                    icon: "success",
                    title: 'Programadores encontradas',
                    didOpen: (toast) => {
                        toast.onmouseenter = Swal.stopTimer;
                        toast.onmouseleave = Swal.resumeTimer;
                    }
                }).fire();

            }

            if (data.length > 0) {
                data.forEach(pro => {
                    const tr = document.createElement('tr');
                    const celda1 = document.createElement('td');
                    const celda2 = document.createElement('td');
                    const celda3 = document.createElement('td');
                    const celda4 = document.createElement('td');
                    const celda5 = document.createElement('td');
                    const celda6 = document.createElement('td');
                    const celda7 = document.createElement('td');
                    const celda8 = document.createElement('td');
                    const celda9 = document.createElement('td');
                    const buttonModificar = document.createElement('button');
                    const buttonEliminar = document.createElement('button');

                    celda1.innerText = contador;
                    celda2.innerText = pro.GRA_NOMBRE;
                    celda3.innerText = pro.ARMA_NOMBRE;
                    celda4.innerText = pro.PRO_NOMBRE;
                    celda5.innerText = pro.PRO_APELLIDO;
                    celda6.innerText = pro.PRO_DPI;
                    celda7.innerText = pro.PRO_CORREO;

                    buttonModificar.textContent = 'Modificar';
                    buttonModificar.classList.add('btn', 'btn-warning', 'w-100');
                    buttonModificar.addEventListener('click', () => llenarDatos(pro));

                    buttonEliminar.textContent = 'Eliminar';
                    buttonEliminar.classList.add('btn', 'btn-danger', 'w-100');
                    buttonEliminar.addEventListener('click', () => eliminar(pro.PRO_ID));

                    celda8.appendChild(buttonModificar);
                    celda9.appendChild(buttonEliminar);

                    tr.appendChild(celda1);
                    tr.appendChild(celda2);
                    tr.appendChild(celda3);
                    tr.appendChild(celda4);
                    tr.appendChild(celda5);
                    tr.appendChild(celda6);
                    tr.appendChild(celda7);
                    tr.appendChild(celda8);
                    tr.appendChild(celda9);
                    fragment.appendChild(tr);

                    contador++;
                });
            } else {
                const tr = document.createElement('tr');
                const td = document.createElement('td');
                td.innerText = 'No hay programadores disponibles';
                td.colSpan = 4 ;

                tr.appendChild(td);
                fragment.appendChild(tr);
            }
        } else {
            console.log('Error al cargar programadores');
        }

        tablaPros.tBodies[0].appendChild(fragment);
    } catch (error) {
        console.log(error);
    }
};



const guardarPro = async (e) => {
    e.preventDefault();
    btnGuardar.disabled = true;

    const url = '/formaD_conJS/controllers/programador/index.php';
    const formData = new FormData(formulario);
    formData.append('tipo', 1);
    formData.delete('pro_id');

    const config = {
        method: 'POST',
        body: formData
    };

    try {
        console.log('Enviando datos:', ...formData.entries());
        const respuesta = await fetch(url, config);
        const data = await respuesta.json();
        console.log('Respuesta recibida:', data);
        const { mensaje, codigo, detalle } = data;
        if (respuesta.ok && codigo === 1) {
            Swal.mixin({
                toast: true,
                position: "top-end",
                showConfirmButton: false,
                timer: 3000,
                timerProgressBar: true,
                icon: "success",
                title: mensaje,
                didOpen: (toast) => {
                    toast.onmouseenter = Swal.stopTimer;
                    toast.onmouseleave = Swal.resumeTimer;
                }
            }).fire();

            formulario.reset();
            getPros(alerta ='no');
        } else {
            console.log('Error:', detalle);
            Swal.mixin({
                toast: true,
                position: "top-end",
                showConfirmButton: false,
                timer: 3000,
                timerProgressBar: true,
                icon: "error",
                title: 'Error al guardar',
                didOpen: (toast) => {
                    toast.onmouseenter = Swal.stopTimer;
                    toast.onmouseleave = Swal.resumeTimer;
                }
            }).fire();
        }
    } catch (error) {
        console.log('Error de conexión:', error);
        Swal.mixin({
            toast: true,
            position: "top-end",
            showConfirmButton: false,
            timer: 3000,
            timerProgressBar: true,
            icon: "error",
            title: 'Error de conexión',
            didOpen: (toast) => {
                toast.onmouseenter = Swal.stopTimer;
                toast.onmouseleave = Swal.resumeTimer;
            }
        }).fire();
    }
    btnGuardar.disabled = false;
};

const llenarDatos = (pro) => {
    formulario.pro_id.value = pro.PRO_ID;
    formulario.pro_grado.value = pro.GRA_ID;
    formulario.pro_arma.value = pro.ARMA_ID;
    formulario.pro_nombre.value = pro.PRO_NOMBRE;
    formulario.pro_apellido.value = pro.PRO_APELLIDO;
    formulario.pro_dpi.value = pro.PRO_DPI;
    formulario.pro_correo.value = pro.PRO_CORREO;
   

    btnLimpiar.parentElement.style.display = 'none';
    btnBuscar.parentElement.style.display = 'none';
    btnCancelar.parentElement.style.display = '';
    btnGuardar.parentElement.style.display = 'none';
    btnModificar.parentElement.style.display = '';
};

